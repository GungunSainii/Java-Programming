import java.util.*;

public class if_else {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a Number");
        int num = sc.nextInt();
        if (num%2 ==0){
            System.out.println("EVEN Number");
        } else {
            System.out.println("ODD Number");
        }

    }
    
}

